    private void botaoEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEntrarActionPerformed
        if (!(nomeTextLogin.getText().equals(""))) {
            int index = bodiesNames.indexOf(nomeTextLogin.getText());
            if (index >= 0) {
                JOptionPane.showMessageDialog(this, "Não foi possível iniciar a seção. O usuário informado já existe.", "Seção não iniciada", ERROR_MESSAGE);
            } else {
                if (botaoRadioInserirLink.isSelected()) {
                    String host = linkTextLogin.getText();
                    int porta = 5000;
                    String nome = nomeTextLogin.getText();
                    bodiesNames.add(nomeTextLogin.getText());
                    conexao = new Conexao(IPTextLogin.getText(), porta, nome);
                    Observador body = new Body(nomeTextLogin.getText(), this, pagina, conexao);
                    this.incluirObservador(body);
                } else {
                    try {
                        InetAddress localHost = InetAddress.getLocalHost();
                        String host = localHost.getHostName();
                        int porta = 8080;
                        String nome = nomeTextLogin.getText();
                        bodiesNames.add(nomeTextLogin.getText());
                        conexao = new Conexao(IPTextLogin.getText(), porta, nome);
                        Observador body = new Body(nomeTextLogin.getText(), this, pagina, conexao);
                        this.incluirObservador(body);
                    } catch (UnknownHostException ex) {
                        Logger.getLogger(InitSection.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Não foi possível iniciar a seção. O usuário não foi informado.", "Seção não iniciada", ERROR_MESSAGE);
        }
        //this.dispose();
    }//GEN-LAST:event_botaoEntrarActionPerformed

