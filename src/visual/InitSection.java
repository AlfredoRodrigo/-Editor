    private void botaoRadioInserirLinkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoRadioInserirLinkActionPerformed
        if (botaoRadioCriarSecao.isSelected()) {
            linkTextLogin.setEnabled(false);
            botaoEntrar.setEnabled(false);
            botaoCriarSecao.setEnabled(true);
        } else {
            linkTextLogin.setEnabled(true);
            botaoEntrar.setEnabled(true);
            botaoCriarSecao.setEnabled(false);
        }
    }//GEN-LAST:event_botaoRadioInserirLinkActionPerformed

