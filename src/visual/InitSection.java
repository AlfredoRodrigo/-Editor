    @Override
    public void incluirObservador(Observador observador) {
        this.bodies.add(observador);
    }
    
    @Override
    public void removerObservador(Observador observador) {
        int index = this.bodies.indexOf(observador);
        if (index >= 0) {
            this.bodies.remove(observador);
        }
    }
    
    @Override
    public void notificarObservadores() {
        for (Observador observador : bodies) {
            observador.atualizar(this.pagina.getStyledDocument());
        }
    }
    
